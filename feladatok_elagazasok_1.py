n = int(input('a hét napjának sorszáma: '))

if n == 1: print(f'a hét {n}. napja hétfő')
elif n == 2: print(f'a hét {n}. napja kedd')
elif n == 3: print(f'a hét {n}. napja szerda')
elif n == 4: print(f'a hét {n}. napja csütörtök')
elif n == 5: print(f'a hét {n}. napja péntek')
elif n == 6: print(f'a hét {n}. napja szombat')
elif n == 7: print(f'a hét {n}. napja vasárnap')
else: print('hibás sorsám')